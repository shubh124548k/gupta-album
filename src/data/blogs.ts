export const getBlogBySlug = (slug: string): Blog | undefined => {
  return blogs.find(b => b.slug === slug);
};

export const getBlogById = (id: string): Blog | undefined => {
  return blogs.find(b => b.id === id);
};

export const getRecentBlogs = (count: number = 3): Blog[] => {
  return [...blogs].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime()).slice(0, count);
};

export const getBlogsByCategory = (category: string): Blog[] => {
  return blogs.filter(b => b.category.toLowerCase() === category.toLowerCase());
};
